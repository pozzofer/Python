cadena = "Estoy utilizando los metodos de Python"
cadena2 = "ESTOY ULTILIZANDO LOS METODOS DE PYTHON "

# metodo lower()
#modifica el comportamiento habitual de la cadena
#vuelve las letras mayusculas a minusculas

print(cadena.lower())
print(cadena2.lower())

#metodo upper()
#modifica el comportamiento habitual de la cadena
#vuelve las letras minusculas a mayusculas

print(cadena.upper())
print(cadena2.upper())

#metodo capitalize
#vuelve la primer letra del string en mayuscula
#si hay alguna mayuscula la convierte en minuscula

print(cadena.capitalize())
print(cadena2.capitalize())

#metodo title
#convierte en mayuscula la primer letra de cada palabra
#si hay alguna mayuscula la convierte en minuscula

print(cadena.title())
print(cadena2.title())

#metodo swapcase
#convierte las mayusculas a minusculas y viceversa

print(cadena.swapcase())
print(cadena2.swapcase())