cadena = "Estoy mostrando los metodos booleanos para las strings"
cadena2 = "JDHKSDHJDSK"

# metodo startswith
#verifica si la cadena empieza con el parametro que se le pasa
print(cadena.startswith("E"))

#metodo endswith
#verifica si la cadena termina con el parametro que se le pasa
print(cadena.endswith("s"))

#metodo isupper
#verifica si toda la cadena esta en mayuscula
print(cadena2.isupper())

#metodo islower
print(cadena2.islower())