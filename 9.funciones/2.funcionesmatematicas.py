# estas funciones estan integradas en una libreria = conjunto de funciones que nosotros podemos mandar a llamar
import math #importar funciones matematicas de math

#funcion pow es para usar la potenciacion
print(math.pow(10 , 2))

#metodo sqrt es para sacar la raiz cuadrada
